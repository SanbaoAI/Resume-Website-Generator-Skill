function getNested(obj, path) {
  return path.split('.').reduce((o, k) => (o && o[k] !== undefined ? o[k] : null), obj);
}

window.applySiteLanguage = function applySiteLanguage(lang) {
  const dict = window.SITE_I18N[lang];
  if (!dict) return;

  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = getNested(dict, el.dataset.i18n);
    if (val != null) el.textContent = val;
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = getNested(dict, el.dataset.i18nHtml);
    if (val != null) el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const val = getNested(dict, el.dataset.i18nAria);
    if (val != null) el.setAttribute('aria-label', val);
  });

  const title = getNested(dict, 'meta.title');
  const desc = getNested(dict, 'meta.description');
  if (title) {
    document.title = title;
    const ogTitle = document.querySelector('meta[property="og:title"]');
    if (ogTitle) ogTitle.setAttribute('content', title);
  }
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && desc) metaDesc.setAttribute('content', desc);
  const ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc && desc) ogDesc.setAttribute('content', desc);

  document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle('is-active', active);
    btn.setAttribute('aria-pressed', String(active));
  });

  try {
    localStorage.setItem('site-lang', lang);
  } catch (_) { /* ignore */ }
};

window.initSiteI18n = function initSiteI18n() {
  const saved = (() => {
    try { return localStorage.getItem('site-lang'); } catch (_) { return null; }
  })();
  const browserEn = navigator.language && navigator.language.startsWith('en');
  const initial = saved || (browserEn ? 'en' : 'zh');

  window.applySiteLanguage(initial);

  document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
    btn.addEventListener('click', () => {
      window.applySiteLanguage(btn.dataset.lang);
    });
  });
};
